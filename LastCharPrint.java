/**
 * LastCharPrint
 */
    class LastCharPrint {

    public static void main(String[] args) {
        String s = "kanita";
        char sh=' ';
        for(int i=0;i<s.length();i++){
            char ch = s.charAt(i);
             sh=ch;
          }
         System.out.println();
         System.out.println(sh );
}
}
